package JavaUdemyTrainning.Functions;

public class DifferenceWhileAndFor {

    public static void main(String[] args) {

        // BOTH CODES DO THE SAME THING

        int i, j = 0;

        for(i = 0; i <= 10; i++){
            System.out.println(i);
        }

        while(j <= 10){
            System.out.println(j);
            j++;
        }

    }
}
